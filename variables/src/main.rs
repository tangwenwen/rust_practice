const MAX_POINTS: u32 = 100_000;

fn main() {
    let mut x = 5;
    println!("the value of x is : {}", x);
    x = 6;
    println!("the value of x is : {}", x);

    println!("const value is :{}", MAX_POINTS);

    let y = 5;
    let y = y + 1;
    let y = y * 2;
    println!("the value of y is :{}", y);
}
